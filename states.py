"""FSM-состояния для диалога о тренировках."""

from aiogram.fsm.state import State, StatesGroup


class WorkoutStates(StatesGroup):
    """Состояния диалога о тренировках."""

    # Бот ждёт от пользователя недостающие данные о подходе (вес, повторения, подходы)
    waiting_for_details = State()

    # Бот ждёт текстовое название упражнения после нажатия "Добавить своё упражнение"
    waiting_for_custom_name = State()


class ProgramStates(StatesGroup):
    """Состояния диалога составления программы тренировки (/program)."""

    # Пользователь ещё не дошёл до конца выбора (способ составления, и если применимо —
    # цель) через inline-кнопки. Сам выбор текстового ввода не требует — состояние нужно
    # только чтобы корректно прервать диалог, если посреди него запустят другую команду
    # (как и у остальных FSM-диалогов, см. handlers.py).
    awaiting_choice = State()


class ProfileStates(StatesGroup):
    """
    Состояния мини-опроса профиля (опыт/оборудование/ограничения) — проходит один раз
    перед первой генерацией программы (см. handlers.cmd_program), либо по явному запросу
    через /update_profile.
    """

    # Ждём свободный текст про стаж тренировок ("полгода", "2 года", "только начинаю")
    waiting_for_experience = State()

    # Оборудование выбирается inline-кнопкой (не требует состояния), но после выбора
    # ждём необязательное уточнение текстом или команду /skip
    waiting_for_equipment_details = State()

    # Ждём свободный текст про травмы/ограничения (либо нажатие кнопки "Нет")
    waiting_for_limitations = State()
