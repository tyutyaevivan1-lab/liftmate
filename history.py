"""
Хранилище последних сообщений переписки с каждым пользователем.

Используется, чтобы передавать GPT контекст последних реплик диалога — это позволяет
понимать короткие дополнения вроде "ещё один подход" или "i made one set more" как
продолжение уже обсуждаемого упражнения, а не как отдельное, бессмысленное сообщение.

Хранится в оперативной памяти процесса (как и FSM-состояние в MemoryStorage) —
это осознанный выбор ради простоты MVP, история не переживает перезапуск бота.

Язык интерфейса пользователя здесь больше НЕ хранится: он явно выбирается через
/language и хранится персистентно в БД (см. database.get_user_language).
"""

from collections import defaultdict, deque

# Сколько последних реплик (и от пользователя, и от бота) хранить на каждого пользователя
HISTORY_SIZE = 6

_history: dict = defaultdict(lambda: deque(maxlen=HISTORY_SIZE))


def add_message(user_id: int, role: str, text: str) -> None:
    """Добавляет реплику в историю переписки пользователя. role: "user" или "bot"."""
    _history[user_id].append({"role": role, "text": text})


def get_history(user_id: int) -> list:
    """Возвращает последние реплики переписки пользователя в хронологическом порядке."""
    return list(_history[user_id])
