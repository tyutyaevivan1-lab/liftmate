"""Вспомогательные функции общего назначения."""


def pluralize_sets(count: int) -> str:
    """
    Возвращает слово "подход" в правильном падеже для русского языка
    в зависимости от количества (count).

    Примеры: 1 -> "подход", 2..4 -> "подхода", 5..20 -> "подходов",
    21 -> "подход", 22..24 -> "подхода" и т.д.
    """
    n = abs(count) % 100

    # 11-14 — особый случай, всегда "подходов"
    if 11 <= n <= 14:
        return "подходов"

    last_digit = n % 10
    if last_digit == 1:
        return "подход"
    if 2 <= last_digit <= 4:
        return "подхода"
    return "подходов"


def pluralize_days(count: int) -> str:
    """
    Возвращает слово "день" в правильном падеже для русского языка в зависимости
    от количества (count) — та же грамматика, что и pluralize_sets, но для дней.

    Примеры: 1 -> "день", 2..4 -> "дня", 5..20 -> "дней", 21 -> "день" и т.д.
    """
    n = abs(count) % 100

    if 11 <= n <= 14:
        return "дней"

    last_digit = n % 10
    if last_digit == 1:
        return "день"
    if 2 <= last_digit <= 4:
        return "дня"
    return "дней"


def suggest_next_weight(weight: float) -> float:
    """
    Предлагает следующий вес для прогрессии: небольшой, реально доступный в зале шаг
    (стандартный блин 1.25кг для лёгких/изолирующих весов, 2.5кг — для более тяжёлых).

    Это примерно 2.5-5% от веса для типичных рабочих весов, но выражено фиксированным
    шагом, а не точным процентом — округлять процент до "разумного" числа для абсолютно
    любого веса было бы менее реалистичной рекомендацией, чем "возьми следующий блин".
    """
    increment = 1.25 if weight <= 20 else 2.5
    return round(weight + increment, 2)
